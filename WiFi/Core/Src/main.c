uint8_t configure_esp()
{
	HAL_Delay(1000);

	if (!esp_send_cmd(&huart1, "AT+CWMODE=1"))
		return 0;
	if (!esp_send_cmd(&huart1, "AT+CWJAP=\"HUAWEI-2.4G-zJ52\",\"...\""))
		return 0;
	if (!esp_send_cmd(&huart1, "AT+CIPMUX=1"))
		return 0;
	if (!esp_send_cmd(&huart1, "AT+CIPSERVER=1,80"))
		return 0;

	return 1;
}
